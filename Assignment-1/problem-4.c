#include <stdio.h>
int main()
{
int n, i;
float input, sum = 0, average;
printf("How many numbers you wish to average : ");
scanf("%d", &n);
printf("Enter %d numbers:\n", n);
for (i = 1; i <= n; i++)
{
scanf("%f", &input);
sum += input;
}
average = sum / n;
printf("AVG of %d inputs: %f\n",n, average);
return 0;
}
